class Solution:
    def knapsack(self, V, n, vw):
        # write code here
        dp = [[0] * (V + 1) for _ in range(n + 1)]  # V + 1 , n + 1 写错
        for i in range(1, n + 1):
            for j in range(1, V + 1):
                w, v = vw[i - 1] # 我想杀了取这个变量名的人
                if j - w >= 0:
                    dp[i][j] = max(dp[i - 1][j], dp[i - 1][j - w] + v)
                else:
                    dp[i][j] = dp[i - 1][j]
        return dp[n][V]


